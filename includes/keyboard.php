<h2>Configurator's Keyboard Shortcuts:</h2>
<p>All of the below shortcuts can be used to toggle between the 
on &amp; off state of the shortcut. For example, pressing the cmd+p 
shortcut will open the preferences dialog and pressing cmd+p again 
will close it. If there are any other shortcuts which would make 
using Configurator easier, please let us know using our Feedback 
form (cmd+e).</p>

<ul>
<li>cmd + p = Preferences</li>
<li>cmd + o = Open your site in new window</li>
<li>cmd + f = Toggle between ullscreen mode</li>
<li>cmd + s = Save your changes</li>
<li>cmd + e = Send feedback (bug or suggestion)</li>

<li>cmd + l = Logout of Configurator</li>
<li>cmd + 0 = Toggle the top shelf</li>
<li>cmd + 1 = Open the Quick Start guide</li>
<li>cmd + 2 = Open the Blocks reference map</li>
<li>cmd + 3 = Open the Position reference map</li>
<li>cmd + 4 = Open the Troubleshooting guide</li>
<li>cmd + 5 = Open the ModuleFX guide</li>
<li>cmd + 6 = Open the PageFX guide</li>
<li>cmd + 7 = Open the MenuFX guide</li>

<li>cmd + 8 = Open the ContentFX guide</li>
</ul>
